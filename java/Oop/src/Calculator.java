public class Calculator {
    // Overloaded add method (two integers)
    public int add(int a, int b) {
        return a + b;
    }

    // Overloaded add method (three integers)
    public int add(int a, int b, int c) {
        return a + b + c;
    }

    // Overloaded add method (two doubles)
    public double add(double a, double b) {
        return a + b;
    }

    // Overloaded add method (three doubles)
    public double add(double a, double b, double c) {
        return a + b + c;
    }

    // Overloaded subtract method (two integers)
    public int subtract(int a, int b) {
        return a - b;
    }

    // Overloaded subtract method (two doubles)
    public double subtract(double a, double b) {
        return a - b;
    }

    // Overloaded multiply method (two integers)
    public int multiply(int a, int b) {
        return a * b;
    }

    // Overloaded multiply method (two doubles)
    public double multiply(double a, double b) {
        return a * b;
    }
}